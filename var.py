a = None  # неопределенное значение

# Numeric Type
i = 1  # integer целое число
f = 2.5  # float число с запятой

# Text Sequence Type
s = 'Hallo'  # string строка

# Boolean Type - True/ False
b1 = True
b2 = False

# Sequence Type
l = [1, 2, 'Olha', True, False]  # Это список. У одной переменной может быть много значений. Например, кто давал Путьке
t = (1, 2, 'Olha', True, False)  # Кортеж. Нельзя изменять. Есть и все...

# Set Types
se = {1, 2, 'Olha', True, False}  # Наз. Множество. Тоже, что и лист. Но,содержит только уникальные значения
fr = {1, 2, 'Olha', True, False}  # Тоже самое, что и множество, но нельзя изменять. Как отличается кортеж от списка

# Mapping Types
d = {'a': 1, 'b': 2}  # Это словарь. Структра - ключ:значение
